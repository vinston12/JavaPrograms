public class switchq {
    public static void main(String[] args) {
        int pierwszaLiczba = 5;
        int drugaMniejszaLiczba = 2;

        for (int i = 0; i <5; i++) {
            if (pierwszaLiczba > drugaMniejszaLiczba){
                System.out.println("Za każdym razem będzie pętla sprawdzała");
            }
            pierwszaLiczba--;
        }
    }
}

